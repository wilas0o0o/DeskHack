  def update
    if @post.update(post_params)
      redirect_to post_path(@post)
    else
      render :edit
    end
  end

  def destroy
    @post.destroy
    redirect_to posts_path
  end

  private

    def posts_params
      params.require(:post).permit(:user_id, :situation_id, :text)
    end

